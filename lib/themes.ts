export function getThemeConfig(key: string | null | undefined): ThemeConfig {
  let lookupKey = key;
  if (key === "slate") lookupKey = "theme1";
  else if (key === "emerald") lookupKey = "theme2";
  else if (key === "purple-sunset") lookupKey = "theme3";
  else if (key === "royal-gold") lookupKey = "theme4";
  else if (key === "rose-wine") lookupKey = "theme5";
  
  return THEMES.find((t) => t.key === lookupKey) || THEMES[0];
}
